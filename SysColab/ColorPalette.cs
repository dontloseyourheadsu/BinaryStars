namespace SysColab;

// <summary>
// A class that defines a color palette with two colors: Dark and Light.
// </summary>
public sealed class ColorPalette
{
    // <summary>
    // Gets the dark primary color of the palette.
    // </summary>
    public static Color DarkPrimary => new(0x655598);

    // <summary>
    // Gets the light primary color of the palette.
    // </summary>
    public static Color LightPrimary => new(0x9880e5);
}